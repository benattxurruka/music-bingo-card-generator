export const SUPPORTED_LANGS = ['en', 'es', 'ca', 'eu'];

export function detectLang() {
  for (const pref of navigator.languages || [navigator.language]) {
    const code = (pref || '').toLowerCase();
    if (SUPPORTED_LANGS.includes(code)) return code;
    const base = code.split('-')[0];
    if (SUPPORTED_LANGS.includes(base)) return base;
  }
  return 'es';
}

let currentLang = detectLang();

export function getCurrentLang() {
  return currentLang;
}

export function t(key, ...args) {
  const val = TRANSLATIONS[currentLang][key];
  return typeof val === 'function' ? val(...args) : (val ?? key);
}

export function applyTranslations(lang) {
  currentLang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = TRANSLATIONS[lang][key];
    if (val !== undefined && typeof val === 'string') el.textContent = val;
  });
  document.getElementById('songs-input').placeholder = t('songs.placeholder');
  document.getElementById('inp-title').placeholder   = t('card.title');
}
